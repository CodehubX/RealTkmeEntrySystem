    private void initFile() {
        File file = new File(FileUtils.getUserHomePath() + "acdb.acc");
        if (!file.exists()) {
            AdminAccount acc = new AdminAccount(Cipher.encrypt("admin"), Cipher.encrypt("artemisadmin"));
            ChromieDB.saveData(file, acc);
        }
        account = (AdminAccount) ChromieDB.loadData(file, AdminAccount.class);
    }

    public static boolean showDialog(Component comp) {
        WebPopOver view = new WebPopOver(comp);
        view.setCloseOnFocusLoss(false);
        view.setModal(true);
        view.setMargin(0);
        AdminLogin log = new AdminLogin(view);
        view.add(log);
        //Center
        int x = Toolkit.getDefaultToolkit().getScreenSize().width / 2 - (int) view.getPreferredSize().getWidth() / 2;
        int y = Toolkit.getDefaultToolkit().getScreenSize().height / 2 - (int) view.getPreferredSize().getHeight() / 2;
        System.out.println(view.getPreferredSize().getWidth());
        view.show(x, y);
        return log.isOk();
    }

