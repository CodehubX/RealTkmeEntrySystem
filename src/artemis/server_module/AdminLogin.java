    private void submit() {
        String un = txtUsername.getText().trim().toLowerCase();
        String pw = txtPassword.getText().trim();
        if (un.isEmpty()) {
            TooltipManager.showOneTimeTooltip(txtUsername, null, IconFactory.infoIcon,
                    "Please enter your username.");
            return;
        }
        if (pw.isEmpty()) {
            TooltipManager.showOneTimeTooltip(txtPassword, null, IconFactory.infoIcon,
                    "Please enter your password.");
            return;
        }
        if (Cipher.decrypt(account.getPassword()).equals(pw) && Cipher.decrypt(account.getUsername()).equals(un)) {
            ok = true;
            popOver.dispose();
        } else {
            ArtemisMessage.showDialog(popOver, "Wrong username/password.");
        }
    }

